
##Using the third "step" argument
fruits = ["apple", "melon", "cherry", "custardapple", "kiwi", "dragonfruit"] 
print(fruits)

print(fruits[::2])

print(fruits[::1])

##Selecting a sublist from a list
fruits = ["apple", "melon", "cherry", "custardapple", "kiwi", "dragonfruit"] 

print(fruits[2:4])

print(fruits[1:])

##Reversing a list with slicing
fruits = ["apple", "melon", "cherry", "custardapple", "kiwi", "dragonfruit"] 
fruits.reverse()
print(fruits)