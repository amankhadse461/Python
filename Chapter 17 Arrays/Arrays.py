fruits = ["mango","apple","grapes","bananan"]
print(fruits)

##Access individual elements through indexes
print(fruits[0])
print(fruits[1])
print(fruits[2])
print(fruits[3])


#append()
print(fruits)
fruits.append("pineapple")
print(fruits)

#Insert()
fruits.insert(0,20)
print(fruits)

#remove()
fruits.remove("apple")
print(fruits)


#pop()
fruits.pop("pineapple")
print(fruits)

#reverse()
fruits.reverse()
print(fruits)

